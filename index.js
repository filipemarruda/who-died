
module.exports.solution = (input) => {

  const resultados = {
    0b1111: [],
    0b0111: [1],
    0b1011: [2],
    0b1101: [3],
    0b1110: [4],
    0b0011: [1,2],
    0b0101: [1,3],
    0b0110: [1,4],
    0b1001: [2,3],
    0b1010: [2,4],
    0b1100: [3,4],
    0b0001: [1,2,3],
    0b0010: [1,2,4],
    0b0100: [1,3,4],
    0b1000: [2,3,4],
    0b0000: [1,2,3,4]
  };
  
  return resultados[input & 0b1111];
}